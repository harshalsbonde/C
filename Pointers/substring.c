#include <stdio.h> 
//////////////////////////////////////////////////////////////
#define TRUE 1 
#define FALSE 0 
///////////////////////////////////////////////////////////////
int cpa_isprefix(char* s1, char* s2)
{
    size_t i;     

    i = 0; 
    while(s1[i] != '\0' && s2[i] != '\0')
    {
        if(s1[i] != s2[i])
            return (FALSE); 
        i = i + 1; 
    }

    return (s2[i] == '\0'); 
}
